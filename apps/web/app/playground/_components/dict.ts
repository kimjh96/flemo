export type PlaygroundLang = keyof typeof playgroundDict;
export type PlaygroundDict = (typeof playgroundDict)[PlaygroundLang];

export function resolvePlaygroundLang(raw: string | null | undefined): PlaygroundLang {
  if (raw === "ko") return "ko";
  return "en";
}

export function format(template: string, vars: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? ""));
}
